-- Set tab indent settings globally
vim.opt.tabstop = 4			-- Display width of tab characters
vim.opt.shiftwidth = 4		-- Width for auto indents
vim.opt.softtabstop = 4		-- Spaces inserted with a <Tab>
vim.opt.expandtab = false	-- Use spaces instead of tabs
vim.opt.autoindent = true	-- Copy indent from current line

-- Others
-- number and relative number combined = the current line shows its absolute number, and other lines show their relative distance from the current line.
vim.opt.number = true			-- Show absolute line numbers
vim.opt.relativenumber = true	-- Show number relative to current line

